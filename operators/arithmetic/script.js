var x = 12;
var y = 4;

var sum = x + y;
console.log("Sum: " + sum); // 16

var difference = x - y;
console.log("Difference: " + difference); // 8

var product = x * y;
console.log("Product: " + product); // 48

var quotient = x / y;
console.log("Quotient: " + quotient); // 3

var remainder = x % y;
console.log("Remainder: " + remainder); // 0

var power = x ** y;
console.log("Power: " + power); // 12^4 = 20736

// Increment example
x++;
console.log("After increment, x = " + x); // 13

// Decrement example
y--;
console.log("After decrement, y = " + y); // 3

// Combined arithmetic
var total = (x + y) * 2;
console.log("Combined total: " + total); // (13 + 3) * 2 = 32
