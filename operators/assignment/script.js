 let x = 10;

    console.log("Initial value of x:", x);

    // Assignment (=)
    x = 10;
    console.log("x = 10 →", x);

    // Addition assignment (+=)
    x += 5; // x = x + 5
    console.log("x += 5 →", x);

    // Subtraction assignment (-=)
    x -= 3; // x = x - 3
    console.log("x -= 3 →", x);

    // Multiplication assignment (*=)
    x *= 2; // x = x * 2
    console.log("x *= 2 →", x);

    // Division assignment (/=)
    x /= 4; // x = x / 4
    console.log("x /= 4 →", x);

    // Modulus assignment (%=)
    x %= 3; // x = x % 3
    console.log("x %= 3 →", x);

    // Exponentiation assignment (**=)
    x **= 2; // x = x ** 2
    console.log("x **= 2 →", x);

    // Example with strings
    let name = "Hanad";
    name += " Ahmed"; // name = name + " Ahmed"
    console.log("name += ' Ahmed' →", name);